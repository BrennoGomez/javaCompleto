package arrays;

public class Arrays1 {
    public static void main(String[] args) {
        int[] idades = new int[4];

        idades[0] = 23;
        idades[1] = 18;
        idades[2] = 52;
        idades[3] = 24;

        System.out.println("Idade posição 0: " + idades[0]);
        System.out.println("Idade posição 1: " + idades[1]);
        System.out.println("Idade posição 2: " + idades[2]);
        System.out.println("Idade posição 3: " + idades[3]);
    }
}
